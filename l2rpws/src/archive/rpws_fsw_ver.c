static char *quoted_string (char *old_token)
{
  int i;
  char *token;
  char *new_token;

  token = &old_token[strlen (old_token) + 2];
  i = 0;
  while (token[i] != '"')
    i++;
  new_token = &token[i];                /* start at quote */
  i = 1;                                /* 1 past quote */
  while (new_token[i] != '"')
    i++;
  i++;                                  /* 1 past quote (again) */
  new_token[i] = 0;
  return new_token;
}

