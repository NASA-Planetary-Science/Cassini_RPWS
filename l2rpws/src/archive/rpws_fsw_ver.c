static int rpws_ver_decode_replace (struct FSW *table, char *token[],
                                    int wbr_flag)
{
  int i;

                                /************************/
  for (i = 0; i < TOKENS; i++)          /* 1st. make sure all       */
    if (!token[i])                      /* 7 tokens are present     */

      return 0;                 /************************/
  i = 4;                                /* 9/22/2003 WTR        */
  while (table[i].version)
    i++;
  table[i].stim_clue = strtol (token[0], NULL, 0);
  table[i].version = strtol (token[1], NULL, 0);
  table[i].patch_level = strtol (token[2], NULL, 0);
  table[i].partition = strtol (token[3], NULL, 0);
  table[i].sclk = strtol (token[4], NULL, 0);
  table[i].sclk_wbr = 0;
  table[i].version_date = rpws_ver_allocate_string (token[5]);
  if (wbr_flag == RPWS_LABEL_WBR) {
    table[i].version_date_wbr = rpws_ver_allocate_string (token[6]);
    table[i].version_string = rpws_ver_allocate_string (token[7]);
  } else {
    table[i].version_date_wbr = rpws_ver_allocate_string (token[5]);
    table[i].version_string = rpws_ver_allocate_string (token[6]);
  }
  table[i + 1].stim_clue = 0;
  table[i + 1].version = 0;
  table[i + 1].patch_level = 0;
  table[i + 1].partition = 0;
  table[i + 1].sclk = 0;
  table[i + 1].sclk_wbr = 0;
  table[i + 1].version_string = NULL;
  table[i + 1].version_date = NULL;
  table[i + 1].version_date_wbr = NULL;

  /*
   *********************************************************
   * pointer should point to something (even a zero-length   *
   *   string), otherwise user is asking to load too         *
   *   much data                                             *
   ***********************************************************/
  if (!table[i].version)
    return 0;

  if (!table[i].sclk)
    table[i].sclk = MDB_time (table[i].version_date);
  table[i].sclk_wbr = MDB_time (table[i].version_date_wbr);
  return 1;
}
