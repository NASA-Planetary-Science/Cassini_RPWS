int rpws_fsw_ver (struct ARCHIVE_TIME_SERIES *archive,
                  int *partition, int *patch_level, char *version_string[])
{
  int i;

  i = rpws_find (fsw, __LINE__);
  while (fsw[i].version) {

          /**********************************************\
         / *  Just in case we add an FSW version	* \
        <  *	line with only a date, go ahead		*  >
         \ *	and do the calculation			* /
          \**********************************************/
    if (!fsw[0].version_date_wbr[0])
      fsw[0].version_date_wbr = fsw[0].version_date;
    if (!fsw[i].sclk)
      fsw[i].sclk = MDB_time (fsw[i].version_date);
    if (!fsw[i].sclk_wbr)
      fsw[i].sclk_wbr = MDB_time (fsw[i].version_date_wbr);

    if (archive->sclk.seconds > fsw[i].sclk) {
      if (partition)
        *partition = fsw[i].partition;
      if (version_string)
        *version_string = fsw[i].version_string;
      if (patch_level)
        *patch_level = fsw[i].patch_level;
      if (archive)
        archive->fsw_ver = fsw[i].version;
      return fsw[i].version;
    }
    i++;
  }
  if (partition)
    *partition = 0;
  if (version_string)
    *version_string = 0;
  if (patch_level)
    *patch_level = 0;
  if (archive)
    archive->fsw_ver = 0;
  return 0;
}
