static void rpws_temp_init_recover (struct FSW *temp)
{
  int i;

  i = rpws_find (temp, __LINE__);
  while (temp[i].version) {
    if (!temp[i].version_date_wbr[0])
      temp[i].version_date_wbr = temp[i].version_date;
    if (!temp[i].sclk)
      temp[i].sclk = MDB_time (temp[i].version_date);
    if (!temp[i].sclk_wbr)
      temp[i].sclk_wbr = MDB_time (temp[i].version_date_wbr);
    i++;
  }
  return;
}

 /*
  *     10  0x4D1CB1A5,   "1998-364T16:17:22.437",
  *           4B000000          <---- start time
  *     11  0x4AE39535,   "1997-298T00:00:24.592",
  *     
  *     if sclk is greate than or equal to table entry, use it
  */
static int rpws_start_scan (struct FSW *temp, int start_sclk)
{
  int i;
  int j;
  static int first = 1;

  if (first) {
    rpws_temp_init_recover (fsw);
    rpws_temp_init_recover (ieb);
    rpws_temp_init_recover (seq);
    rpws_temp_init_recover (mpn);
    rpws_temp_init_recover (target);
    rpws_temp_init_recover (coordinate);
    rpws_temp_init_recover (orbit);
    first = 0;
  }
  i = rpws_find (temp, __LINE__);
  j = i;
  while (temp[i].version) {
    if (start_sclk >= temp[i].sclk) {
      return i;
    }
    i++;
  }
  return j;
}

 /*
  *     10  0x4D1CB1A5,   "1998-364T16:17:22.437",
  *           4B000000          <---- stop time
  *     11  0x4AE39535,   "1997-298T00:00:24.592",
  *     
  *     if sclk is greater than or equal to table entry, use it
  */
static char *rpws_temp (struct FSW *temp, char *start_scet, char *stop_scet,
                        int wbr_flag)
{
  char buffer[16384];
  volatile int i;
  int start_sclk;
  int stop_sclk;

  buffer[0] = 0;
  start_sclk = MDB_time (start_scet);
  stop_sclk = MDB_time (stop_scet);

  i = rpws_start_scan (temp, start_sclk);

  strcat (buffer, temp[i].version_string);
  i--;
  if (!i)
    return rpws_mallocate_string (buffer);

  while (stop_sclk >= get_sclkf (wbr_flag, &temp[i])) {
    if (temp[i].version_string) {
      if (temp[i].version_string[0]) {
        strcat (buffer, ",");
        strcat (buffer, temp[i].version_string);
      }
    }
    i--;
    if (i < 2)
      break;
  }
  return rpws_mallocate_string (buffer);
}
static char *rpws_sort_merge (char *names)
{
  int i, j;
  int icnt = 0;
  char *token[256];
  char *temp[2];
  char *ctemp = { "" };
  char *zzz = { "ZZZZZZZZ" };
  char buffer[16384];
  static char delim[] = { "," };

  memset (buffer, 0, 16384);
  token[0] = strtok (names, delim);
  token[1] = NULL;
  while (token[icnt]) {
    token[icnt + 1] = strtok (NULL, delim);
    icnt++;
    if (icnt > 254)
      break;
  }

  token[icnt] = zzz;
  token[icnt + 1] = zzz;
  token[icnt + 2] = NULL;

  if (!token[1]) {
    strcat (buffer, ",\"");
    strcat (buffer, token[0]);
    strcat (buffer, "\"");
    return rpws_mallocate_string (&buffer[1]);
  }

  if (icnt > 1) {
    for (i = 0; i < icnt + 1; i++) {
      for (j = i + 1; j < icnt + 2; j++) {
        if (strcmp (token[i], token[j]) > 0) {
          temp[0] = token[i];
          temp[1] = token[j];
          token[i] = temp[1];
          token[j] = temp[0];
        }
      }
    }
    for (i = 0; i < icnt; i++) {        /* was -1 */
      if (strcmp (ctemp, token[i])) {
        ctemp = token[i];
        strcat (buffer, ",\"");
        strcat (buffer, token[i]);
        strcat (buffer, "\"");
      }
    }
  } else if (icnt == 1) {
    strcat (buffer, ",\"");
    strcat (buffer, token[0]);
    strcat (buffer, "\"");
  }
  return rpws_mallocate_string (&buffer[1]);
}
static int rpws_strip (char *buffer)
{
  int len;

  len = strlen (buffer);
  if (buffer[len - 3] == ',')
    if (buffer[len - 2] == '"')
      if (buffer[len - 1] == '"')
        buffer[len - 3] = 0;
  return 0;

}
int rpws_quote_strip (char *buffer)
{
  int i = 0;
  int j = 0;

  for (i = 0; i < strlen (buffer); i++)
    if (buffer[i] != '"')
      buffer[j++] = buffer[i];
  buffer[j] = 0;
}
